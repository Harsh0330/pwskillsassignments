#include <iostream>

using namespace std;

int main()
{
  int a = 6, b = 3;
  cout << "a+b = " << a + b << endl;
  cout << "a-b = " << a - b << endl;
  cout << "a*b = " << a * b << endl;
  cout << "a/b = " << a / b << endl;
  cout << "a%b = " << a % b << endl;
  cout << "a++ = " << a++ << endl;
  cout << "a-- = " << a-- << endl;
  cout << "++a = " << ++a << endl;
  cout << "--a = " << --a << endl;
  cout << "a+=b = " << a += b << endl;
  cout << "a-=b = " << a -= b << endl;
  cout << "a*=b = " << a *= b << endl;
  cout << "a/=b = " << a /= b << endl;
  cout << "a%=b = " << a %= b << endl;
  // cout << "a==b = " << (a == b) << endl;
  cout << "a!=b = " << (a != b) << endl;
  cout << "a>b = " << (a > b) << endl;
  cout << "a<b = " << (a < b) << endl;
  cout << "a>=b = " << (a >= b) << endl;
  cout << "a<=b = " << (a <= b) << endl;
  cout << "a&&b = " << (a && b) << endl;
  cout << "a||b = " << (a || b) << endl;
  cout << "!a = " << !a << endl;
  cout << "!b = " << !b << endl;
  return 0;
}